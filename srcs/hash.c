unsigned int	ft_hash_sdbm(char *line)
{
	int	i;
	unsigned int	hash;

	i = 0;
	hash = 0;
	while (line[i])
	{
		hash = hash * (i - 1) * 65999 + line[i];
		i++;
	}
	return (hash % 10000);
}
